export const hasRedundantIcon = () => {
  const idSet = new Set();
  const labelSet = new Set();
  const iconSet = new Set();

  for (const icon of icons) {
    if (idSet.has(icon.id)) {
      throw new Error(`Duplicate ID found: ${icon.id}`);
    }
    if (labelSet.has(icon.label)) {
      throw new Error(`Duplicate label found: ${icon.label}`);
    }
    if (iconSet.has(icon.value)) {
      throw new Error(`Duplicate icon found: ${icon.value}`);
    }

    idSet.add(icon.id);
    labelSet.add(icon.label);
    iconSet.add(icon.value);
  }
};
export const orderedIcons = icons.sort((a, b) =>
  a.label.localeCompare(b.label),
);
